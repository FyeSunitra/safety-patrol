export type InspectionStatus = "normal" | "abnormal" | "not_relevant";

export interface InspectionItem {
  id: string;
  category: string;
  name: string;
  status: InspectionStatus;
  details?: string;
  images?: string[];
  recommendations?: string;
  responsible?: string;
  responsibleOther?: string;
  isCustom?: boolean;
}

export interface InspectionRecord {
  id: string;
  date: string;
  time?: string;
  building: string;
  floor: string;
  division: string;
  department?: string;
  surveyTeam: string[];
  inspectorName?: string;
  items: InspectionItem[];
  createdAt: string;
  updatedAt: string;
}

